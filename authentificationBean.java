package Beans;

public class authentificationBean {
public String nom;
public String prenom;
public authentificationBean() {
	
	setNom("");
	setPrenom("");
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public String getPrenom() {
	return prenom;
}
public void setPrenom(String prenom) {
	this.prenom = prenom;
}


}
